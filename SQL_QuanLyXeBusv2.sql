-- Bảng thông tin tỉnh
CREATE TABLE TINH
(
    maTinh      VARCHAR(10)     PRIMARY KEY,
    tenTinh     NVARCHAR(50)    NOT NULL
);
GO

-- Bảng thông tin huyện
CREATE TABLE HUYEN
(
    maHuyen     VARCHAR(10)     PRIMARY KEY,
    tenHuyen    NVARCHAR(50)    NOT NULL,
    idTinh      VARCHAR(10)     FOREIGN KEY REFERENCES TINH(maTinh)
);
GO

-- Bảng thông tin điểm đi qua
CREATE TABLE DIEMDIQUA
(
    maDiemDiQua     VARCHAR(10)     PRIMARY KEY,
    tenDiemDiQua    NVARCHAR(50)    NOT NULL,
    idHuyen         VARCHAR(10)     FOREIGN KEY REFERENCES HUYEN(maHuyen)
);
GO

-- Bảng thông tin nhà xe
CREATE TABLE NHAXE
(
    maNhaXe         VARCHAR(10)     PRIMARY KEY,
    tenNhaXe        NVARCHAR(50)    NOT NULL,
    diaChi          NVARCHAR(100)   NOT NULL,
    soDienThoai     VARCHAR(15)     NOT NULL,
    email           VARCHAR(100)    NOT NULL,
    idHuyen         VARCHAR(10)     FOREIGN KEY REFERENCES HUYEN(maHuyen)
);
GO

-- Bảng thông tin chuyến đi
CREATE TABLE CHUYENDI
(
    maChuyenDi      VARCHAR(10)     PRIMARY KEY,
    idNhaXe         VARCHAR(10)     FOREIGN KEY REFERENCES NHAXE(maNhaXe),
    tenChuyenDi     NVARCHAR(50)    NOT NULL,
    soLuongGhe      INT             NOT NULL,
    giaVe           FLOAT           NOT NULL -- Thêm cột giá vé mặc định cho chuyến đi
);
GO

-- Bảng thông tin ghế
CREATE TABLE GHE
(
    maGhe           VARCHAR(10)     PRIMARY KEY,
    soGhe           VARCHAR(10)     NOT NULL, -- Số ghế hiển thị (ví dụ: A1, B2)
    idChuyenDi      VARCHAR(10)     FOREIGN KEY REFERENCES CHUYENDI(maChuyenDi),
    trangThai       INT             NOT NULL DEFAULT 0 -- 0: trống, 1: đã đặt
);
GO

-- Bảng liên kết giữa chuyến đi và điểm đi qua
CREATE TABLE CHUYENDI_DIEMDIQUA
(
    idChuyenDi      VARCHAR(10)     FOREIGN KEY REFERENCES CHUYENDI(maChuyenDi),
    idDiemDiQua     VARCHAR(10)     FOREIGN KEY REFERENCES DIEMDIQUA(maDiemDiQua),
    thuTuDiQua      INT             NOT NULL,
    ngayGioDiQua       DATETIME        NOT NULL,
    thoiGianDuKien  DATETIME        NOT NULL
);
GO

-- Bảng thông tin người dùng
CREATE TABLE NGUOIDUNG
(
    maNguoiDung     VARCHAR(10)     PRIMARY KEY,
    tenNguoiDung    NVARCHAR(50)    NOT NULL,
    email           VARCHAR(100)    NOT NULL,
    ngaySinh        DATE,
    otpCode         VARCHAR(6),
    otp_taoThoiGian DATETIME
);
GO

-- Bảng thông tin đặt vé
-- Một người dùng (idNguoiDung) có thể đặt nhiều ghế bằng cách tạo nhiều bản ghi trong DATVE
CREATE TABLE DATVE
(
    maDatVe         VARCHAR(10)     PRIMARY KEY,
    idNguoiDung     VARCHAR(10)     FOREIGN KEY REFERENCES NGUOIDUNG(maNguoiDung),
    idDiemBatDau    VARCHAR(10)     FOREIGN KEY REFERENCES DIEMDIQUA(maDiemDiQua),
    idDiemKetThuc   VARCHAR(10)     FOREIGN KEY REFERENCES DIEMDIQUA(maDiemDiQua),
    idGhe           VARCHAR(10)     FOREIGN KEY REFERENCES GHE(maGhe),
    ngayDat         DATE            NOT NULL,
	trangThai       INT             NOT NULL, -- 0: chưa xác nhận, 1: đã xác nhận, 2: đã hủy
    soTienPhaiTra   FLOAT           NOT NULL
);
GO

